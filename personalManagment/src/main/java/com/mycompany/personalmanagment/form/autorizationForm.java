    public String ot() {
        String login = jTextField1.getText();
        String password = jTextField2.getText();

        OkHttpClient okHttpClient = new OkHttpClient();

//        FormBody formBody = new FormBody.Builder()
//                .add("login", login)
//                .add("password", password)
//                .build();

        String url = "http://localhost:8080/api/v1/employee/SignIn" +
                "?login=" + login +
                "&password=" + password;

        Request request = new Request.Builder()
                .url(url)
                .get()//подумать
                .build();

        try (Response response = okHttpClient.newCall(request).execute()) {
            JSONArray ja = new JSONArray(response.body().string());
            if (ja!=null) {
                jLabel3.setText("Удачно");
                JOptionPane.showMessageDialog(jDialog1, "Удачно!", "Вы вошли", JOptionPane.INFORMATION_MESSAGE);
                new mainForm().setVisible(true);
            } else {
                JOptionPane.showMessageDialog(jDialog1, "Вы неправильно ввели логин или пароль!", "Вы не вошли", JOptionPane.ERROR_MESSAGE);
            }
        } catch (IOException ex) {
            Logger.getLogger(autorizationForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        return null;
    }

