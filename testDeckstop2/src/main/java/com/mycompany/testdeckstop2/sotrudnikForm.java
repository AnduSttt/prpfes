    public void AddInformation() {
        Request req = new Request.Builder()
                .url("http://localhost:8080/api/v1/sotrudnik" + "?id=" + sotrudnikId)
                .get()
                .build();

        try (Response res = ok.newCall(req).execute()) {

            JSONObject jo = new JSONObject(res.body().string());
            System.err.println(jo);
            jTextField1.setText(jo.getString("familiya"));
            jTextField2.setText(jo.getString("imya"));
            jTextField3.setText(jo.getString("otchestvo"));
            jTextField3.setText(jo.getString("otchestvo"));
            jTextField4.setText(jo.getString("corporatEmail"));
            jFormattedTextField1.setText(jo.getString("denRoshdeniya"));

        } catch (IOException ex) {
            Logger.getLogger(sotrudnikForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void CreateComboBoxElement() {

        for (Object el : dolshstList) {
            jComboBox2.addItem((String) el);
        }

        for (Object el : departList) {
            jComboBox1.addItem((String) el);
        }

        jComboBox2.setSelectedIndex(departId - 1);

    }

